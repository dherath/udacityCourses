public class ArithmaticOperators{
	public static void main(String[] args) {
		int x =3;
		int y =4;
		int xy = 11;
		int cos = 5;

		double a = ((x+1*4)*(2+y));
		double b = x + y/2 +xy;
		double c = (4+cos)/2;

		System.out.println("a->"+a);
		System.out.println("b->"+b);
		System.out.println("c->"+c);
	}
}